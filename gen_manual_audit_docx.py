def set_font(doc: Document) -> None:
    style = doc.styles["Normal"]
    style.font.name = "宋体"
    style._element.rPr.rFonts.set(qn("w:eastAsia"), "宋体")
    style.font.size = Pt(11)


def main() -> Path:
    doc = Document()
    set_font(doc)
    sec = doc.sections[0]
    sec.top_margin = Cm(2.54)
    sec.bottom_margin = Cm(2.54)
    sec.left_margin = Cm(2.0)
    sec.right_margin = Cm(2.0)

    t = doc.add_paragraph()
    t.alignment = WD_ALIGN_PARAGRAPH.CENTER
    r = t.add_run("说明书与当前项目代码出入对照表")
    r.bold = True
    r.font.size = Pt(16)
    r.font.name = "黑体"
    r._element.rPr.rFonts.set(qn("w:eastAsia"), "黑体")

    doc.add_paragraph(
        "对照文档：0612(2).docx\n"
        "对照基准：etf_agent 当前生产代码"
    )

    headers = ["问题在哪", "问题是什么", "正确信息应该是什么"]
    tbl = doc.add_table(rows=1 + len(ROWS), cols=3)
    tbl.style = "Table Grid"
    for j, h in enumerate(headers):
        tbl.rows[0].cells[j].text = h
    for i, (where, problem, correct) in enumerate(ROWS, 1):
        tbl.rows[i].cells[0].text = where
        tbl.rows[i].cells[1].text = problem
        tbl.rows[i].cells[2].text = correct

    doc.add_paragraph()
    doc.add_heading("备注", level=2)
    doc.add_paragraph(
        "上表共 20 条。第 1～6 条建议答辩前优先修改说明书。"
        "综合评分 25/10/30/20/15、新鲜新闻 15:00 切分、经济日 85/75/65、"
        "单 ETF 30%、轮动惩罚、日历完全为空 50% 硬顶等与代码一致，未列入。"
    )

    OUT.parent.mkdir(parents=True, exist_ok=True)
    doc.save(OUT)
    print(f"已生成: {OUT}")
    return OUT


